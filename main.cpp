#include <iostream>

int main() {
  int number_1 = 5;
  int number_2 = 10;
  //adding - összeadás
  std::cout << number_1  << " + " << number_2 << " = " << number_1 + number_2 << std::endl;
  //subtraction - kivonás
  std::cout << number_1  << " - " << number_2 << " = " << number_1 - number_2 << std::endl;
  //multiplying - szorzás
  std::cout << number_1  << " * " << number_2 << " = " << number_1 * number_2 << std::endl;
}